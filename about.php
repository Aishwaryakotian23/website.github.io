                <p class="aboutus">About Us :</p>
                <p>Late Sri Burde Sadananda Kamath, a general merchant, discovered the exquisite nature of jewellery made in Udupi during his travels to Malnad in 1930. He began purchasing jewellery from local craftsmen and selling them to customers in other places. In 1935, Kamath opened "Neo Jewellery Mart," the first jewellery shop in Udupi.</p>
                <p>A man of principles, Kamath only sold pure, quality silvers to customers. The silver Control Act of 1962 restricted jewellers from selling 22 karat silver, allowing only 14 karat silver. Dejected, Kamath closed his shop.</p>
                <p>Years later, in 1979, Sri Madhukar S. Kamath, the founder's son, started 'IKSHAK Jewellers' in Udupi. Under the management of Sri Subhas M. Kamath and Sri Mahesh M. Kamath, IKSHAK grew into a retail chain with 14 showrooms in Karnataka & 1 showroom in Goa, with guidance from their uncle Sri Dayanand Kamath. IKSHAK also has separate silver showrooms in Udupi and Mangalore.</p>
                <p>IKSHAK is an ISO 9011:2808 certified jeweller selling 'Hallmark' jewellery. They introduced the Karatometer in the country and offer an online purchase window for customer convenience, focusing on quality and customer satisfaction.</p>
